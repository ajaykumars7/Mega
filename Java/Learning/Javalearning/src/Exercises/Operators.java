package Exercises;

public class Operators {
    public static void main(String[] args) {
        // Swap two Numbers
        int num1 = 7;
        int num2 = 11;
        int temp;

        temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("num1 = " + num1 + "\nnum2 = " + num2);

    }
}
